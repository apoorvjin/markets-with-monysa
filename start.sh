#!/usr/bin/env bash
set -euo pipefail

# в”Җв”Җ Colors в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
GREEN='\033[0;32m'; BLUE='\033[0;34m'; YELLOW='\033[1;33m'
RED='\033[0;31m'; BOLD='\033[1m'; NC='\033[0m'

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
cd "$SCRIPT_DIR"

echo -e "${BOLD}${BLUE}"
echo "  в–Ҳв–Ҳв–Ҳв•—   в–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв•—   в–Ҳв–Ҳв•—в–Ҳв–Ҳв•—   в–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— "
echo "  в–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв•— в–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•”в•җв•җв•җв•җв•қв–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—"
echo "  в–Ҳв–Ҳв•”в–Ҳв–Ҳв–Ҳв–Ҳв•”в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в–Ҳв–Ҳв•— в–Ҳв–Ҳв•‘ в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қ в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•‘"
echo "  в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘  в•ҡв–Ҳв–Ҳв•”в•қ  в•ҡв•җв•җв•җв•җв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•‘"
echo "  в–Ҳв–Ҳв•‘ в•ҡв•җв•қ в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•‘ в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘"
echo "  в•ҡв•җв•қ     в•ҡв•җв•қ в•ҡв•җв•җв•җв•җв•җв•қ в•ҡв•җв•қ  в•ҡв•җв•җв•җв•қ   в•ҡв•җв•қ   в•ҡв•җв•җв•җв•җв•җв•җв•қв•ҡв•җв•қ  в•ҡв•җв•қ"
echo -e "${NC}"
echo -e "${BOLD}  Global Markets Intelligence вҖ” Local Dev Launcher${NC}"
echo ""

# в”Җв”Җ Load .env в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
if [ -f ".env" ]; then
  echo -e "${GREEN}  Loading .env${NC}"
  set -a
  # shellcheck disable=SC1091
  source .env
  set +a
else
  echo -e "${YELLOW}  No .env found вҖ” using defaults (OpenAI/Finnhub features disabled)${NC}"
fi

# в”Җв”Җ Config в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
export PORT="${PORT:-5001}"
export EXPO_PUBLIC_DOMAIN="${EXPO_PUBLIC_DOMAIN:-localhost:$PORT}"

echo ""
echo -e "  ${BOLD}Backend port:${NC}  ${PORT}"
echo -e "  ${BOLD}Frontend URL:${NC}  http://localhost:8081"
echo -e "  ${BOLD}API domain:${NC}    ${EXPO_PUBLIC_DOMAIN}"
echo ""

# в”Җв”Җ Kill stale processes в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
for port in "$PORT" 8081; do
  pid=$(lsof -ti ":$port" 2>/dev/null || true)
  if [ -n "$pid" ]; then
    echo -e "${YELLOW}  Clearing port $port (PID $pid)...${NC}"
    kill -9 "$pid" 2>/dev/null || true
    sleep 0.3
  fi
done

# в”Җв”Җ Start Express backend в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
echo -e "${BLUE}  Starting Express backend...${NC}"

SERVER_LOG="/tmp/monysa-server.log"

USING_OSASCRIPT=false

if command -v osascript &>/dev/null; then
  # macOS: open a dedicated Terminal window for the server.
  # Pass SCRIPT_DIR as an arg so the heredoc can be single-quoted (no bash expansion
  # inside AppleScript, which chokes on em dashes and unescaped special chars).
  osascript - "$SCRIPT_DIR" 2>/dev/null <<'APPLESCRIPT' && USING_OSASCRIPT=true || USING_OSASCRIPT=false
on run argv
  set scriptDir to item 1 of argv
  tell application "Terminal"
    activate
    do script "cd " & quoted form of scriptDir & " && npm run server:dev"
  end tell
end run
APPLESCRIPT
fi

if [ "$USING_OSASCRIPT" = false ]; then
  # Fallback: background process, logs to file
  npm run server:dev > "$SERVER_LOG" 2>&1 &
  SERVER_PID=$!
  echo -e "  Server PID: ${SERVER_PID}  (logs: ${SERVER_LOG})"
fi

# в”Җв”Җ Wait for backend в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
echo -ne "  Waiting for backend"
for i in $(seq 1 40); do
  if nc -z localhost "$PORT" 2>/dev/null; then
    echo -e " ${GREEN}ready!${NC}"
    break
  fi
  if [ "$i" -eq 40 ]; then
    echo -e " ${RED}timed out!${NC}"
    if [ "${USING_OSASCRIPT:-false}" = false ]; then
      echo ""
      echo -e "${RED}  Server logs:${NC}"
      cat "$SERVER_LOG" 2>/dev/null || true
    fi
    exit 1
  fi
  echo -n "."
  sleep 0.5
done

# в”Җв”Җ Cleanup trap (for background-mode only) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
if [ "${USING_OSASCRIPT:-false}" = false ]; then
  cleanup() {
    echo -e "\n${YELLOW}  Shutting down...${NC}"
    kill "$SERVER_PID" 2>/dev/null || true
  }
  trap cleanup EXIT INT TERM
fi

# в”Җв”Җ Start Expo в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
echo ""
echo -e "${BOLD}${GREEN}  Backend running. Starting Expo...${NC}"
echo -e "${BLUE}  Press ${BOLD}w${NC}${BLUE} вҶ’ open in browser В· ${BOLD}i${NC}${BLUE} вҶ’ iOS Simulator В· ${BOLD}a${NC}${BLUE} вҶ’ Android emulator${NC}"
echo ""

EXPO_PUBLIC_DOMAIN="$EXPO_PUBLIC_DOMAIN" npx expo start --localhost
