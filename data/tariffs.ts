export function getSortedTariffs(data: CountryTariff[], ascending: boolean = false): CountryTariff[] {
  return [...data].sort((a, b) => {
    const diff = ascending ? a.tariffRate - b.tariffRate : b.tariffRate - a.tariffRate;
    if (diff !== 0) return diff;
    return a.countryName.localeCompare(b.countryName);
  });
}

export function getCountryFlag(countryCode: string): string {
  if (countryCode === 'EU') return '\u{1F1EA}\u{1F1FA}';
  const codePoints = countryCode
    .toUpperCase()
    .split('')
    .map((char) => 0x1f1e6 + char.charCodeAt(0) - 65);
  return String.fromCodePoint(...codePoints);
}

export function getTariffColor(rate: number): string {
  if (rate >= 40) return '#FF4D6A';
  if (rate >= 25) return '#FFB84D';
  if (rate >= 15) return '#00D4AA';
  return '#8A94A6';
}

export function formatDate(dateString: string): string {
  const date = new Date(dateString);
  return date.toLocaleDateString('en-US', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
  });
}

export function formatBillions(amount: number): string {
  if (amount >= 1000) {
    return `$${(amount / 1000).toFixed(1)}T`;
  }
  if (amount < 1) {
    return `$${(amount * 1000).toFixed(0)}M`;
  }
  return `$${amount.toFixed(1)}B`;
}
